/*Create a Student Object
Make an object student with properties: name, age, course, isEnrolled.
Print each property using dot and bracket notation.*/


let student = {
    name : "vanshika",
    age : 21,
    course : "bca",
    isEnrolled : true
}

// Dot notation
console.log(student.name)
console.log(student.age)

// Bracket notaion
console.log(student['course'])
console.log(student['isEnrolled'])

